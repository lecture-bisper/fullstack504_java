package quiz;

// 문제 9) Calculator 클래스를 만들어 더하기(add)와 곱하기(multiply) 메서드를 작성하세요.
//각 메서드는 두 개의 정수를 입력받아 결과를 반환합니다.
//객체를 생성하고 메서드를 호출하여 결과를 출력하세요.

public class Calculator {

  public int add(int a, int b) {
    return a + b;
  }

  public int multiply(int a, int b) {
    return a * b;
  }
}












